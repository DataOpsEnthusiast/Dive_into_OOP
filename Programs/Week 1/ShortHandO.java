public class ShortHandO {
    public static void main(String[] args) {
        int j, p, q, r, s, t;
        j = 5;
        p = 1;
        q = 2;
        r = 3;
        s = 4;
        t = 5;
        p += j;
        q -= j;
        r *= j;
        s /= j;
        t %= j;
        System.out.println("p = " + p);
        System.out.println("q = " + q);
        System.out.println("r = " + r);
        System.out.println("s = " + s);
        System.out.println("t = " + t);
    }
}
