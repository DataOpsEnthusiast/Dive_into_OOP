public class SimpleArithmetic {
    public static void main(String[] args) {
        int j, k, p, q, r, s, t;
        j = 5;
        k = 2;
        p = j + k;
        q = j - k;
        r = j * k;
        s = j / k;
        t = j % k;
        System.out.println("p = " + p);
        System.out.println("q = " + q);
        System.out.println("r = " + r);
        System.out.println("s = " + s);
        System.out.println("t = " + t);
    }

}
