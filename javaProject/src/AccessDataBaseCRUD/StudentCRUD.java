    public void LoadJTableStudent(){
        String query = "Select * from students";
        Connect con = new Connect(); 
        con.openConnection();
        ResultSet rs = con.Search(query);
        DefaultTableModel model = (DefaultTableModel) this.jTableStudent.getModel();
        for(int i=this.jTableStudent.getRowCount()-1;i>=0;i--){
            model.removeRow(i);
        }
        try {
            while(rs.next()){
                model.addRow(
                    new Object[]{
                        rs.getInt("student_iD"),
                        rs.getString("student_name"),
                        rs.getInt("student_roll_number"),
                        rs.getString("student_phone_number"),
                        rs.getString("student_gender"),
                        rs.getString("student_degree"),
                    }
            );
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(StudentCRUD.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            con.closeConnection();
        }
       
}
    public void JLoadComboBoxDegree(){
        String query = "Select * FROM Degrees";
        Connect con = new Connect();
        con.openConnection();
        ResultSet rs = con.Search(query);
        
        try {
            while(rs.next()){
                jComboBoxDegree.addItem(rs.getString("degree_title"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudentCRUD.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            con.closeConnection();
        }
    }
    
