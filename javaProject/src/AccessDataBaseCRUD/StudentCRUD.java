    private void jButtonAddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddStudentActionPerformed
        int rollNumber = Integer.parseInt(jTextFieldRollNumber.getText());
        String name = jTextFieldName.getText();
        String phoneNumber = jTextFieldPhoneNumber.getText();
        String gender = null;
        if(jRadioButtonMale.isSelected()){
            gender = jRadioButtonMale.getText();
        }else{
            gender = jRadioButtonFemale.getText();
        } 
        String degree = jComboBoxDegree.getSelectedItem().toString();
        Student stu = new Student(name,rollNumber,phoneNumber,gender,degree);
        if(stu.addStudent()){
            JOptionPane.showMessageDialog(null,"Student Record inserted successfully");
        }else{
            JOptionPane.showMessageDialog(null,"Student Record not inserted successfully");           
        }
        LoadJTableStudent();
    }//GEN-LAST:event_jButtonAddStudentActionPerformed

