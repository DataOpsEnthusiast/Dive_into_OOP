    private void jButtonSerachStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSerachStudentActionPerformed
        int rollNumber = Integer.parseInt(jTextFieldRollNumber.getText());
       String query ="Select * from students where student_roll_number = '"+rollNumber+"'";
        Connect con = new Connect();
        con.openConnection();
        ResultSet rs = con.Search(query);
        try {
            if(rs.next()){
                jTextFieldRollNumber.setText(rs.getInt("student_roll_number")+"");
                jTextFieldName.setText(rs.getString("student_name"));
                jTextFieldPhoneNumber.setText(rs.getString("student_phone_number"));
                String gender = null;
                if(rs.getString("student_gender").equals("male")){
                    jRadioButtonMale.setSelected(true);
                }else{
                    jRadioButtonFemale.setSelected(true);
                }
                jComboBoxDegree.setSelectedItem(rs.getString("student_degree"));
            }} catch (SQLException ex) {
            Logger.getLogger(StudentCRUD.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            con.closeConnection();
        }
    }//GEN-LAST:event_jButtonSerachStudentActionPerformed

